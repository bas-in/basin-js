describe("openAPIDocToTypes — non-table paths ignored", () => {
  const doc: OpenAPIDocument = makeDoc({
    paths: {
      "/rest/v1/users": {
        get: {
          operationId: "getUsers",
          responses: {
            "200": {
              description: "OK",
              content: {
                "application/json": {
                  schema: {
                    type: "array",
                    items: {
                      type: "object",
                      required: ["id"],
                      properties: { id: { type: "integer" } },
                    },
                  },
                },
              },
            },
          },
        },
      },
      "/rest/v1/users/rpc/my_func": {
        get: {
          operationId: "myFunc",
          responses: { "200": { description: "OK" } },
        },
      },
    },
  });

  it("nested paths (not matching /rest/v1/<table>) are ignored", () => {
    const out = openAPIDocToTypes(doc);
    expect(out).toContain("users:");
    expect(out).not.toContain("my_func:");
  });
});
